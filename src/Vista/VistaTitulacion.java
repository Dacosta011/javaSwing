    private void guardar()
    {
        //proceso de guardar:
        String id,tit,uni,esp;
        boolean ok;
        
        id=this.jTextField1.getText();
        tit=this.jTextField2.getText();
        uni=this.jTextField3.getText();
        esp=this.jTextField4.getText();
        
        Titulacion titulacion=new Titulacion(id, tit, uni, esp);
        
        ok=this.gt.guardaTitulacion(titulacion);
        if(ok)
        {
            JOptionPane.showMessageDialog(null,"OPERACIÓN EXITOSA..!!");
        }
        else
            JOptionPane.showMessageDialog(null,"OPERACIÓN FALLIDA..!!");
    }
    
    @Override
    public void actionPerformed(ActionEvent e) 
    {
       if(e.getSource()==this.jButton1)
        {
           new VistaUniversidad();
        }
        if(e.getSource()==this.jButton2)//Crear Nuevo
        {
            new VistaEspecialidad();
        }
        if(e.getSource()==this.jButton3)//Crear Nuevo
        {
            this.guardar();
        }
        if(e.getSource()==this.jButton6)//Crear Nuevo
        {
            this.setVisible(false);
            new Principal();
        }
        
    }
    
    
